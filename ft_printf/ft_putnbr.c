#include "ft_printf.h"

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		ft_putchar('-');
		nb = -nb;
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
		ft_putchar(nb + 48);
}

void	ft_putunsnbr(unsigned int unnb)
{
	if (unnb > 9)
	{
		ft_putunsnbr(unnb / 10);
		ft_putunsnbr(unnb % 10);
	}
	else
		ft_putchar(unnb + 48);
}
